export interface DiningItem {
    id: string
    name: string
    category: string
    subCategory: string // e.g. "鸡", "鸭", "饮料", "酒"
    icon: string // Keeping icon for compatibility/fallback, but image is preferred
    image?: string // New image property
    price: number
    description: string
}

